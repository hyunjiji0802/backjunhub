## 세단, SUV 중에 기간 내 대여 가능한 차 명단 확인하기
WITH TMP AS (
SELECT 
    c.CAR_ID, c.CAR_TYPE, 30*DAILY_FEE AS TOTAL_FEE
    # c.CAR_ID,
    # c.CAR_TYPE,
    # c.DAILY_FEE,
    # ch.CAR_ID AS CD,
    # ch.START_DATE,
    # ch.END_DATE
FROM CAR_RENTAL_COMPANY_CAR c
LEFT JOIN CAR_RENTAL_COMPANY_RENTAL_HISTORY ch
ON c.CAR_ID = ch.CAR_ID
AND START_DATE <='2022-11-30 00:00:00'
AND END_DATE >='2022-11-01 00:00:00'
WHERE CAR_TYPE IN ("SUV", "세단")
AND ch.CAR_ID IS NULL
# 2022년 11월 1일부터 2022년 11월 30일 대여가능

)

# SELECT * FROM TMP

SELECT 
    CAR_ID,
    CAR_TYPE,
    TOTAL_FEE - ROUND(TOTAL_FEE * DISCOUNT_RATE * 0.01, 0) AS FEE
FROM TMP
JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN 
USING (CAR_TYPE)
#30일 이상 대여 할인율
WHERE DURATION_TYPE = "30일 이상"
AND TOTAL_FEE - ROUND(TOTAL_FEE * DISCOUNT_RATE * 0.01, 0) >=500000
AND TOTAL_FEE - ROUND(TOTAL_FEE * DISCOUNT_RATE * 0.01, 0) <2000000
ORDER BY FEE DESC, CAR_TYPE, CAR_ID DESC